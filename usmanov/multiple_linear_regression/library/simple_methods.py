def sqrt(x):
    # Проверяем частные случаи:
    if x < 0:
        raise ValueError("Квадратный корень из отрицательного числа не определен")
    elif x == 0:
        return 0
    elif x == 1:
        return 1

    # Устанавливаем начальное приближение для квадратного корня
    guess = x / 2
    # Устанавливаем точность до 10^-10
    precision = 10 ** -10
    # Выполняем итерационный метод Ньютона для вычисления квадратного корня
    while abs(guess ** 2 - x) > precision:
        guess = (guess + x / guess) / 2
    return guess